function toggleCourseNav() {
    const courseNav = document.getElementById('courseNav');
    if (courseNav.style.display === 'none' || courseNav.style.display === '') {
        courseNav.style.display = 'block';
    } else {
        courseNav.style.display = 'none';
    }
}

